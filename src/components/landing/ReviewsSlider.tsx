const CARD_MIN_WIDTH = 320
const CARD_GAP = 16
const STEP_SCROLL = CARD_MIN_WIDTH + CARD_GAP

function ReviewCard({ review }: { review: Review }) {
	return (
		<article className='flex min-h-[260px] min-w-[calc(100vw-4rem)] max-w-[calc(100vw-4rem)] flex-shrink-0 snap-start rounded-2xl border border-[var(--surface)] bg-[var(--bg-content)] p-5 md:min-h-[220px] md:min-w-[320px] md:max-w-[320px] md:p-6'>
			<div className='flex min-h-0 flex-1 flex-col'>
				{/* Верхняя строка: имя слева, звёзды справа */}
				<div className='mb-3 flex items-start justify-between gap-2'>
					<span
						className='truncate font-medium text-[var(--text)]'
						title={review.name}
					>
						{review.name}
					</span>
					<span
						className='flex flex-shrink-0 text-amber-500'
						aria-label={`Рейтинг: ${review.rating ?? 5} из 5`}
					>
						{'★'.repeat(review.rating ?? 5)}
					</span>
				</div>
				{/* Текст отзыва — фиксированная высота (как 5 строк), скролл при длинном тексте */}
				<div className='scrollbar-theme max-h-[9rem] overflow-y-auto pr-1 md:max-h-[7.5rem]'>
					<p className='text-sm leading-relaxed text-[var(--text)] md:text-base'>
						«{review.text}»
					</p>
				</div>
				{/* Нижняя строка: дата слева, источник справа */}
				<div className='mt-4 flex flex-shrink-0 flex-wrap items-center justify-between gap-2'>
					{review.date ?
						<span className='text-sm text-[var(--text-muted)]'>
							{review.date}
						</span>
					:	<span aria-hidden />}
					<a
						href={review.sourceUrl}
						target='_blank'
						rel='noopener noreferrer'
						className='text-sm text-[var(--accent)] hover:underline'
					>
						{review.source}
					</a>
				</div>
			</div>
		</article>
	)
}

export function ReviewsSlider() {
	const sectionRef = useRef<HTMLElement>(null)
	const scrollRef = useRef<HTMLDivElement>(null)

	const scroll = (direction: 'prev' | 'next') => {
		const el = scrollRef.current
		if (!el) return
		const delta = direction === 'next' ? STEP_SCROLL : -STEP_SCROLL
		el.scrollBy({ left: delta, behavior: 'smooth' })
	}

	useEffect(() => {
		const section = sectionRef.current
		if (!section) return
		const ctx = gsap.context(() => {
			gsap.fromTo(
				section.querySelector('h2'),
				{ opacity: 0, y: 16 },
				{
					opacity: 1,
					y: 0,
					duration: 0.5,
					scrollTrigger: { trigger: section, start: 'top 85%' },
				}
			)
		}, section)
		return () => ctx.revert()
	}, [])

	return (
		<section ref={sectionRef} className='scroll-section'>
			<div className='mx-auto max-w-2xl rounded-2xl bg-[var(--bg)]/90 px-4 py-6 shadow-xl backdrop-blur-sm'>
				<h2 className='section-title'>
					Отзывы клиентов
				</h2>
				<p className='mx-auto mt-2 max-w-xl text-center text-sm text-[var(--text-muted)]'>
					Нас рекомендуют на 2ГИС и Яндекс.Картах
				</p>
			</div>
			<div className='relative mx-auto mt-5 w-full'>
				<button
					type='button'
					onClick={() => scroll('prev')}
					className='absolute left-0 top-1/2 z-10 hidden h-10 w-10 -translate-y-1/2 cursor-pointer items-center justify-center rounded-full border border-[var(--surface)] bg-[var(--bg-content)] text-[var(--text)] shadow-md transition hover:bg-[var(--surface)] disabled:opacity-40 disabled:cursor-not-allowed md:left-2 md:flex md:h-12 md:w-12'
					aria-label='Предыдущие отзывы'
				>
					<svg
						className='h-5 w-5 md:h-6 md:w-6'
						fill='none'
						stroke='currentColor'
						viewBox='0 0 24 24'
						aria-hidden
					>
						<path
							strokeLinecap='round'
							strokeLinejoin='round'
							strokeWidth={2}
							d='M15 19l-7-7 7-7'
						/>
					</svg>
				</button>
				<button
					type='button'
					onClick={() => scroll('next')}
					className='absolute right-0 top-1/2 z-10 hidden h-10 w-10 -translate-y-1/2 cursor-pointer items-center justify-center rounded-full border border-[var(--surface)] bg-[var(--bg-content)] text-[var(--text)] shadow-md transition hover:bg-[var(--surface)] disabled:opacity-40 disabled:cursor-not-allowed md:right-2 md:flex md:h-12 md:w-12'
					aria-label='Следующие отзывы'
				>
					<svg
						className='h-5 w-5 md:h-6 md:w-6'
						fill='none'
						stroke='currentColor'
						viewBox='0 0 24 24'
						aria-hidden
					>
						<path
							strokeLinecap='round'
							strokeLinejoin='round'
							strokeWidth={2}
							d='M9 5l7 7-7 7'
						/>
					</svg>
				</button>
				<div
					ref={scrollRef}
					className='scrollbar-theme flex gap-4 overflow-x-auto pb-2 scroll-smooth md:px-2'
					style={{
						scrollSnapType: 'x mandatory',
						WebkitOverflowScrolling: 'touch',
					}}
				>
					{REVIEWS.map((review, i) => (
						<ReviewCard key={i} review={review} />
					))}
				</div>
			</div>
		</section>
	)
}
